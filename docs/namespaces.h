/**
 *
 * \namespace OpenPSTD
 * \brief This is the general namespace of the OpenPSTD application. 
 *
 * OpenPSTD consists of three submodules: the backend (Kernel), the command line interface (CLI) and the graphical user interface (GUI)
 */

/**
 *
 * \namespace OpenPSTD::Kernel
 * \brief The Kernel is responsible for the scientific computions in openPSTD.
 *
 * The Kernel consists of the functions used in the PSTD method, as well as the representations of the computational domain.
 */


/**
 *
 * \namespace OpenPSTD::GUI
 * \brief The GUI displays the current state of the simulation and provides the possibility to modify the settings.
 */


/**
 *
 * \namespace OpenPSTD::CLI
 * \brief The CLI provides the possibility to run and check the simulation from a command line without any graphical feedback.
 *
 * The CLI is useful for running on remote (multicore) machines.
 */

/**
 *
 * \namespace OpenPSTD::Shared
 * \brief This is the namespace for all components shared between the application that call the Kernel (most notably the CLI and the GUI)
 *
 */
